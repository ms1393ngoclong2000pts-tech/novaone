  key_result VARCHAR(220) NOT NULL,
  level VARCHAR(30) NOT NULL,
  owner VARCHAR(120) NOT NULL,
  progress TINYINT UNSIGNED NOT NULL DEFAULT 0,
  status VARCHAR(30) NOT NULL,
  created_at TIMESTAMP NULL,
  updated_at TIMESTAMP NULL
);

CREATE TABLE shipments (
  id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  code VARCHAR(60) NOT NULL UNIQUE,
  `order` VARCHAR(60) NOT NULL,
  carrier VARCHAR(120) NOT NULL,
  status VARCHAR(30) NOT NULL,
  eta DATE NULL,
  created_at TIMESTAMP NULL,
  updated_at TIMESTAMP NULL
);

CREATE TABLE calendar (
  id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  title VARCHAR(180) NOT NULL,
  type VARCHAR(40) NOT NULL,
  owner VARCHAR(120) NOT NULL,
  date DATE NOT NULL,
  status VARCHAR(30) NOT NULL,
  created_at TIMESTAMP NULL,
  updated_at TIMESTAMP NULL
);

CREATE TABLE facilities (
  id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  code VARCHAR(60) NOT NULL UNIQUE,
  name VARCHAR(160) NOT NULL,
  type VARCHAR(40) NOT NULL,
  location VARCHAR(160) NOT NULL,
  status VARCHAR(30) NOT NULL,
  created_at TIMESTAMP NULL,
  updated_at TIMESTAMP NULL
);

CREATE TABLE permissions (
  id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  role VARCHAR(80) NOT NULL,
  module VARCHAR(120) NOT NULL,
  can_view VARCHAR(10) NOT NULL DEFAULT 'no',
  can_create VARCHAR(10) NOT NULL DEFAULT 'no',
  can_update VARCHAR(10) NOT NULL DEFAULT 'no',
  can_delete VARCHAR(10) NOT NULL DEFAULT 'no',
  created_at TIMESTAMP NULL,
  updated_at TIMESTAMP NULL
);

CREATE TABLE settings (
  id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  `key` VARCHAR(120) NOT NULL UNIQUE,
  value VARCHAR(255) NOT NULL,
  `group` VARCHAR(60) NOT NULL,
  status VARCHAR(30) NOT NULL,
  created_at TIMESTAMP NULL,
  updated_at TIMESTAMP NULL
);
